def search_symptoms(query, language='vi'):
    """
    Tìm kiếm triệu chứng bằng cả tiếng Việt và tiếng Anh
    
    Args:
        query: Từ khóa tìm kiếm
        language: 'vi' hoặc 'en'
    
    Returns:
        List các triệu chứng phù hợp (tiếng Anh)
    """
    query = query.lower()
    results = []
    
    if language == 'vi':
        # Tìm trong tiếng Việt
        for en, vi in SYMPTOMS_VI.items():
            if query in vi.lower() or query in en.lower():
                results.append(en)
    else:
        # Tìm trong tiếng Anh
        for en in SYMPTOMS_VI.keys():
            if query in en.lower():
                results.append(en)
    
    return results